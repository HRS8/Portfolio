import React, { useEffect, useState } from "react";
import { Link } from "react-router-dom";

import { styles } from "../styles";
import { navLinks } from "../constants";
import { logol, menu, close } from "../assets";

const Navbar = () => {
  const [active, setActive] = useState("");
  const [toggle, setToggle] = useState(false);
  const [scrolled, setScrolled] = useState(false);

  useEffect(() => {
    const handleScroll = () => {
      const scrollTop = window.scrollY;
      if (scrollTop > 100) {
        setScrolled(true);
      } else {
        setScrolled(false);
      }
    };

    window.addEventListener("scroll", handleScroll);

    return () => window.removeEventListener("scroll", handleScroll);
  }, []);
  return (
    <nav className="font-Source Code Pro fixed z-20">
      <nav className={`${styles.paddingX}  w-full flex py-5 fixed top-0 z-20 ${scrolled? "transition ease-linear delay-70 duration-[900ms] backdrop-blur-lg bg-white bg-opacity-10 backdrop-shadow-lg ":"bg-transparent"}`}>
        <div className="w-full flex  justify-start max-w-7xl mx-auto md:justify-between">
          <Link to="/" className="flex items-center gap-2 right-0 flex-row md:flex-row" onClick={()=>{
            setActive("");
            window.scrollTo(0,0);
          }}>
            <img src={logol} alt='logo' className="rounded-xl hover:rounded-lg w-9 h-9 object-contain"/>
            <p className="hover:text-blue-300 text-white text-[18px] font-bold cursor-pointer flex ">
              Harsha&nbsp;<span className="sm:block hidden">| WebDev</span>
            </p>
          </Link>
          <ul className='list-none hidden sm:flex flex-row gap-10 items-center right-0 '>
          {navLinks.map((nav) => (
            <li
              key={nav.id}
              className={`${
                active === nav.title ? "text-white" : "text-secondary"
              } hover:text-white-100 hover:text-[20px] duration-200 text-[18px] font-medium cursor-pointer`}
              onClick={() => setActive(nav.title)}
            >
              <a href={`#${nav.id}`}>{nav.title}</a>
            </li>
          ))}
        </ul>
        <div className='sm:hidden flex flex-1 justify-end items-center '>
          <img
            src={toggle ? close : menu}
            alt='menu'
            className='w-[28px] h-[28px] object-contain'
            onClick={() => setToggle(!toggle)}
          />

          <div
            className={`${
              !toggle ? "hidden" : "flex"
            } p-6 bg-gradient-to-br border border-[#3f3e45] from-black-200 to-gray-600 absolute top-20 right-0 mx-4 my-2 min-w-[140px] z-10 rounded-xl `}
          >
            <ul className='list-none flex justify-end items-start flex-1 flex-col gap-4'>
              {navLinks.map((nav) => (
                <li
                  key={nav.id}
                  className={`font-source-code-pro font-medium cursor-pointer text-[16px] ${
                    active === nav.title ? "text-white" : "text-secondary"
                  }`}
                  onClick={() => {
                    setToggle(!toggle);
                    setActive(nav.title);
                  }}
                >
                  <a href={`#${nav.id}`}>{nav.title}</a>
                </li>
              ))}
            </ul>
          </div>
          </div>
        </div>
      </nav>
    </nav>
  );
};

export default Navbar;

